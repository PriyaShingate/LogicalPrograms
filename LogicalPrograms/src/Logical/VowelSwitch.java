package Logical;

public class VowelSwitch {
	public static void main(String[] args) {
		
		char ch='o';
		
		switch(ch) {
		 
		 case 'a':
			 System.out.println("Vowel");
			 break;
		 case 'e':
			 System.out.println("vowel");
			 break;
		 case 'i':
			 System.out.println("vowel");
			 break;
		 case 'o':
			 System.out.println("vowel");
			 break;
		 case 'u':
			 System.out.println("vowel");
			 break;
		
		default:
		System.out.println("Given character is not vowel");
	}

}
}